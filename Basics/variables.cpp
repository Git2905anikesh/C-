#include <iostream>
using namespace std;
int main (){
    // integers data type
    int apples = 5;
    cout<< apples<<endl;
    cout<< "size of int:" <<sizeof(apples)<< endl;

    // char data type
    char sign = '&';
    cout<< sign<<endl;
    cout<< "size of char:" <<sizeof(sign)<< endl; 

     // boolen data type
    bool flag = true;
    cout<< flag<<endl;
    cout<< "size of bool:" <<sizeof(flag)<< endl; 

     // float data type
    float value = 3.14;
    cout<< value<<endl;
    cout<< "size of float:" <<sizeof(value)<< endl; 






    return 0;
}